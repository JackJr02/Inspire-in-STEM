#Area of a circle

pi=3.142
radius=7
area=pi*radius*radius

print ("The area of the circle is " + str(area ) + "cm.sq")


#Volume of a cube
length=4
volume=length*length*length

print("The volume of the cube is" + str(volume) + "cm.sq")
 


 #Volume of a cylinder
radius=7
height=4
PI=3.142
Vol= PI*radius*radius*height

print ("The volume of the cylinder is " + str(Vol) + "cm.sq")

#Surface Area of a Cylinder

pI=3.142
radius=7
diameter=14
height=3.5
surfaceArea=2*pI*radius*radius + pI*diameter*height

print("The surface area of the cylinder is " + str(surfaceArea) + "cm.sq")




